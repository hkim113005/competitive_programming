#include <iostream>
#include <math.h>

using namespace std;

int n;
int m = 1000000007;

int main() {
    cin >> n;

    int next = 0;
    long long ans = 0;
    for (int i = 1; i * i <= n; i++) {
        ans += ((n / i) * i) % m;
        ans %= m;
        next = (n / i) % m;
    }

    next--;
    for (int i = 1; i <= next; i++) {
        int x = n / i, y = n / (i + 1) + 1;
        x %= m, y %= m;
        ans += ((((x + y) % m) * ((x - y + 1) % m)) % m / 2) * i % m;
        ans %= m;
    }

    cout << ans << endl;

    return 0;
}

